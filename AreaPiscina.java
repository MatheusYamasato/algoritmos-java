class AreaPiscina {
    public static void main(
        String[] args) {
            // raio da piscina
            double raio = 2;
            //area da piscina
            double areap;
            // valor do pi
            areap = Math.PI * Math.pow(raio, 2);
            System.out.println("A área da piscina é de " + areap);
        }
}
